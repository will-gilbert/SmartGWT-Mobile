package com.smartgwt.mobile.client.types;

/**
 * Enumeration of standard operations that can be performed when a built-in button is
 * clicked.
 */
public enum DefaultDialogAction {
    /**
     * Do nothing / take no action.
     */
    DO_NOTHING,

    /**
     * Hide the dialog.
     */
    HIDE,

    /**
     * Hide the dialog and then destroy it.
     */
    DESTROY
}
